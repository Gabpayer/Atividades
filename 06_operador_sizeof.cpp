#include <iostream>
using namespace std;

int main() {
    int a;
    double b;
    char c;
    cout << "Tamanho de int: " << sizeof(a) << " bytes" << endl;
    cout << "Tamanho de double: " << sizeof(b) << " bytes" << endl;
    cout << "Tamanho de char: " << sizeof(c) << " byte" << endl;
    return 0;
}
